package com.app.strings;

public class Caracteres {

	public static void main(String[] args) {
		char f = 'f';
		char t = 't';
		char d = 'd';
		char g = 'g';
		char j = 'j';
		char i = 'i';
		//Ejercicio: escribe un programa que muestre por pantalla los caracteres individuales de
		//una determinada palabra utilizando variables de tipo char.
		System.out.println(f);
		System.out.println(t);
		System.out.println(d);
		System.out.println(g);
		System.out.println(j);
		System.out.println(i);
		//Forma con operador de asignación. 
		String nombre1 = "Alejandro";
		System.out.println(nombre1);
		// otra forma de declarar un String
		String nombre2 = new String("Marcos");
		System.out.println(nombre2);
	}

}
